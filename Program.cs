            patchedData = ApplyPatches(patchedData, flashPattern);
            Console.WriteLine($"SRAM patches applied for: {flashPattern.FlashType}");
        }
        else
        {
            Console.WriteLine("No SRAM patterns found - ROM may already use SRAM");
        }

        return patchedData;
    }

    private byte[] ApplyBatteryPatches(byte[] romData, string batteryMode)
    {
        InitializePatterns();

        int romSize = romData.Length;

        const int MAX_ROM_SIZE = 0x02000000; // 32MB
        if (romSize > MAX_ROM_SIZE)
        {
            throw new Exception($"ROM too large ({romSize} bytes). Max size is {MAX_ROM_SIZE} bytes.");
        }

        const int ALIGNMENT = 0x40000; // 256KB

        if (romSize % ALIGNMENT != 0)
        {
            Console.WriteLine("ROM has been trimmed and is misaligned. Padding to 256KB alignment.");
            int originalSize = romSize;
            romSize = ((romSize / ALIGNMENT) + 1) * ALIGNMENT;

            if (romSize > MAX_ROM_SIZE)
            {
                throw new Exception($"ROM too large after padding ({romSize} bytes). Max size is {MAX_ROM_SIZE} bytes.");
            }

            byte[] paddedRomData = new byte[romSize];
            for (int i = 0; i < paddedRomData.Length; i++)
                paddedRomData[i] = 0xFF;
            Array.Copy(romData, paddedRomData, originalSize);
            romData = paddedRomData;
        }

        // Check if already patched
        if (FindBytes(romData, signature) != -1)
        {
            throw new Exception("Signature found. ROM already patched!");
        }

        // Patch IRQ handler address references
        byte[] oldIrqAddr = new byte[] { 0xfc, 0x7f, 0x00, 0x03 };
        byte[] newIrqAddr = new byte[] { 0xf4, 0x7f, 0x00, 0x03 };
        int foundIrq = 0;

        for (int i = 0; i <= romSize - oldIrqAddr.Length; i += 4)
        {
            if (FindBytes(romData.Skip(i).Take(oldIrqAddr.Length).ToArray(), oldIrqAddr) == 0)
            {
                foundIrq++;
                Console.WriteLine($"Found IRQ handler reference at 0x{i:X}, patching");
                Array.Copy(newIrqAddr, 0, romData, i, newIrqAddr.Length);
            }
        }

        if (foundIrq == 0)
        {
            Console.WriteLine("Could not find any reference to the IRQ handler. Has the ROM already been patched?");
        }

        // Find location to insert payload
        int payloadBase = -1;
        const int calculationPayloadLen = 2160;

        for (int i = romSize - ALIGNMENT - calculationPayloadLen; i >= 0; i -= ALIGNMENT)
        {
            bool isAllZeroes = true;
            bool isAllOnes = true;

            for (int j = 0; j < ALIGNMENT + calculationPayloadLen; j++)
            {
                if (i + j >= romSize)
                {
                    isAllZeroes = false;
                    isAllOnes = false;
                    break;
                }

                if (romData[i + j] != 0)
                    isAllZeroes = false;
                if (romData[i + j] != 0xFF)
                    isAllOnes = false;
            }

            if (isAllZeroes || isAllOnes)
            {
                payloadBase = i;
                break;
            }
        }

        if (payloadBase == -1)
        {
            Console.WriteLine("ROM too small to install payload.");
            
            // Check if we can expand the ROM
            if (romSize + ALIGNMENT * 2 > MAX_ROM_SIZE)
            {
                throw new Exception("ROM already max size. Cannot expand. Cannot install payload.");
            }

            Console.WriteLine("Expanding ROM.");
            int originalSize = romSize;
            romSize += ALIGNMENT * 2;
            byte[] expandedRomData = new byte[romSize];
            for (int k = 0; k < expandedRomData.Length; k++)
                expandedRomData[k] = 0xFF;
            
            Array.Copy(romData, expandedRomData, originalSize);
            romData = expandedRomData;

            // Recalculate payloadBase after expansion
            payloadBase = romSize - ALIGNMENT - calculationPayloadLen;

            // Verify the new location is suitable
            bool isAllZeroes = true;
            bool isAllOnes = true;
            for (int j = 0; j < ALIGNMENT + calculationPayloadLen; j++)
            {
                if (payloadBase + j >= romSize)
                {
                    isAllZeroes = false;
                    isAllOnes = false;
                    break;
                }

                if (romData[payloadBase + j] != 0)
                    isAllZeroes = false;
                if (romData[payloadBase + j] != 0xFF)
                    isAllOnes = false;
            }

            if (!(isAllZeroes || isAllOnes))
            {
                throw new Exception("Expanded ROM location is not suitable for payload.");
            }

            Console.WriteLine($"ROM expanded to 0x{romSize:X} bytes. Payload will be injected at 0x{payloadBase:X}");
        }
        else
        {
            Console.WriteLine($"Payload will be injected at 0x{payloadBase:X}");
        }

        // Insert payload (only first 2160 bytes, matching JavaScript behavior)
        Array.Copy(embeddedPayloadBin, 0, romData, payloadBase, calculationPayloadLen);

        Console.WriteLine($"Payload injected at 0x{payloadBase:X}");

        // Patch the ROM entrypoint to jump to the payload
        byte[] originalEntrypointBytes = BitConverter.GetBytes(BitConverter.ToUInt32(romData, 0));
        uint originalEntrypointInstruction = BitConverter.ToUInt32(romData, 0);
        uint originalEntrypointOffset = (originalEntrypointInstruction & 0x00FFFFFF) << 2;
        uint originalEntrypointAddress = 0x08000000 + 8 + originalEntrypointOffset;

        Console.WriteLine($"Original entrypoint instruction: 0x{originalEntrypointInstruction:X8}, offset: 0x{originalEntrypointOffset:X}, address: 0x{originalEntrypointAddress:X}");

        // Write original entrypoint to payload
        Array.Copy(BitConverter.GetBytes(originalEntrypointAddress), 0, romData, payloadBase + PAYLOAD_OFFSETS["ORIGINAL_ENTRYPOINT_ADDR"], 4);

        // Set flush mode
        int flushMode = batteryMode == "auto" ? 0 : 1;
        Array.Copy(BitConverter.GetBytes(flushMode), 0, romData, payloadBase + PAYLOAD_OFFSETS["FLUSH_MODE"], 4);
        Console.WriteLine($"Selected mode: {(flushMode == 0 ? "Auto" : "Keypad")}");

        // Calculate new entrypoint address
        uint patchedEntrypointOffset = BitConverter.ToUInt32(embeddedPayloadBin, PAYLOAD_OFFSETS["PATCHED_ENTRYPOINT"]);
        uint newEntrypointAddress = 0x08000000 + (uint)payloadBase + patchedEntrypointOffset;
        Console.WriteLine($"New entrypoint address: 0x{newEntrypointAddress:X}");

        // Patch ROM entrypoint (ARM branch instruction)
        uint newEntrypointOffsetEncoded = (newEntrypointAddress - 0x08000008) >> 2;
        uint newBranchInstruction = 0xea000000 | newEntrypointOffsetEncoded;
        Array.Copy(BitConverter.GetBytes(newBranchInstruction), 0, romData, 0, 4);

        // Now search for write function signatures and patch them
        bool foundWriteLocation = false;

        for (int i = 0; i <= romSize - 64; i += 2)
        {
            byte[] signatureMatch = null;
            string patchType = null;
            uint saveSize = 0;
            int patchOffset = 0;
            int payloadHookOffset = 0;

            // Check for each signature
            if (MatchesSignature(romData, i, writeSramSignature))
            {
                patchType = "thumb";
                saveSize = 0x8000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_SRAM_PATCHED"];
                Console.WriteLine($"WriteSram identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeSram2Signature))
            {
                patchType = "thumb";
                saveSize = 0x8000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_SRAM_PATCHED"];
                Console.WriteLine($"WriteSram 2 identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeSramRamSignature))
            {
                patchType = "arm";
                saveSize = 0x8000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_SRAM_PATCHED"];
                Console.WriteLine($"WriteSramFast identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeEepromSignature))
            {
                patchType = "thumb";
                saveSize = 0x2000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_EEPROM_PATCHED"];
                Console.WriteLine($"EEPROM write identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeFlashSignature))
            {
                patchType = "thumb";
                saveSize = 0x10000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_FLASH_PATCHED"];
                Console.WriteLine($"Flash write 1 identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeFlash2Signature))
            {
                patchType = "thumb";
                saveSize = 0x10000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_FLASH_PATCHED"];
                Console.WriteLine($"Flash write 2 identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeFlash3Signature))
            {
                patchType = "thumb";
                saveSize = 0x20000;
                patchOffset = 0;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_FLASH_PATCHED"];
                Console.WriteLine($"Flash write 3 identified at offset 0x{i:X}, patching");
            }
            else if (MatchesSignature(romData, i, writeEepromV111Signature))
            {
                patchType = "thumb_epilogue";
                saveSize = 0x2000;
                patchOffset = 12;
                payloadHookOffset = PAYLOAD_OFFSETS["WRITE_EEPROM_V111_POSTHOOK"];
                Console.WriteLine($"EEPROM V111 epilogue identified at offset 0x{i:X}, patching");
            }

            if (patchType != null)
            {
                foundWriteLocation = true;
                // Set save size
                Array.Copy(BitConverter.GetBytes((uint)saveSize), 0, romData, payloadBase + PAYLOAD_OFFSETS["SAVE_SIZE"], 4);
                Console.WriteLine($"Save size set to 0x{saveSize:X}");

                if (flushMode == 0) // Only patch in auto mode
                {
                    if (patchType == "thumb")
                    {
                        Array.Copy(thumbBranchThunk, 0, romData, i + patchOffset, thumbBranchThunk.Length);
                        uint jumpAddress = 0x08000000 + (uint)payloadBase + BitConverter.ToUInt32(embeddedPayloadBin, payloadHookOffset);
                        Array.Copy(BitConverter.GetBytes(jumpAddress), 0, romData, i + patchOffset + thumbBranchThunk.Length, 4);
                        Console.WriteLine($"Patched Thumb function at 0x{i + patchOffset:X} to jump to 0x{jumpAddress:X}");
                    }
                    else if (patchType == "arm")
                    {
                        Array.Copy(armBranchThunk, 0, romData, i + patchOffset, armBranchThunk.Length);
                        uint jumpAddress = 0x08000000 + (uint)payloadBase + BitConverter.ToUInt32(embeddedPayloadBin, payloadHookOffset);
                        Array.Copy(BitConverter.GetBytes(jumpAddress), 0, romData, i + patchOffset + armBranchThunk.Length, 4);
                        Console.WriteLine($"Patched ARM function at 0x{i + patchOffset:X} to jump to 0x{jumpAddress:X}");
                    }
                    else if (patchType == "thumb_epilogue")
                    {
                        Array.Copy(writeEepromV11EpiloguePatch, 0, romData, i + patchOffset, writeEepromV11EpiloguePatch.Length);
                        uint jumpAddress = 0x08000000 + (uint)payloadBase + BitConverter.ToUInt32(embeddedPayloadBin, payloadHookOffset);
                        // Write at i + patchOffset - 1 to match JavaScript behavior
                        Array.Copy(BitConverter.GetBytes(jumpAddress), 0, romData, i + patchOffset - 1, 4);
                        Console.WriteLine($"Patched Thumb epilogue function at 0x{i + patchOffset:X} to jump to 0x{jumpAddress:X}");
                    }
                }
            }
        }

        if (!foundWriteLocation)
        {
            if (flushMode == 0) // Auto mode
            {
                Console.WriteLine("Warning: Could not find any write function signatures. Are you sure the game has save functionality and has been SRAM patched with GBATA?");
            }
            else // Keypad mode
            {
                Console.WriteLine("Warning: Unsure what save type this is. Defaulting to 128KB save");
                Array.Copy(BitConverter.GetBytes((uint)0x20000), 0, romData, payloadBase + PAYLOAD_OFFSETS["SAVE_SIZE"], 4);
                Console.WriteLine("Save size defaulted to 0x20000");
            }
        }

        Console.WriteLine("Battery patches completed successfully");

        return romData;
    }

    private bool MatchesSignature(byte[] data, int offset, byte[] signature)
    {
        if (offset + signature.Length > data.Length)
            return false;

        for (int i = 0; i < signature.Length; i++)
        {
            if (data[offset + i] != signature[i])
                return false;
        }
        return true;
    }

    private FlashPatternInfo GetFlashPattern(byte[] data)
    {
        foreach (var flashPattern in flashPatterns)
        {
            int position = GetPosition(data, flashPattern.IdentPattern.Pattern);
            if (position != -1)
            {
                Console.WriteLine($"Flash type identified: {flashPattern.FlashType}");
                return flashPattern;
            }
        }
        return null;
    }

    private byte[] ApplyPatches(byte[] data, FlashPatternInfo flashPattern)
    {
        byte[] patchedData = (byte[])data.Clone();

        for (int i = 0; i < flashPattern.PatchCount; i++)
        {
            var patch = flashPattern.Patches[i];
            int position = GetPosition(patchedData, patch.Marker.Pattern);

            if (position != -1)
            {
                Console.WriteLine($"Applying patch {i + 1} at position 0x{position:X}");
                Array.Copy(patch.Replace.Pattern, 0, patchedData, position, patch.Replace.PatternLength);
            }
            else
            {
                Console.WriteLine($"Warning: Didn't find pattern #{i + 1} for {flashPattern.FlashType}");
            }
        }

        return patchedData;
    }

    private int GetPosition(byte[] data, byte[] pattern)
    {
        for (int i = 0; i <= data.Length - pattern.Length; i++)
        {
            bool match = true;
            for (int j = 0; j < pattern.Length; j++)
            {
                // 0x00 in pattern is wildcard
                if (pattern[j] != 0x00 && data[i + j] != pattern[j])
                {
                    match = false;
                    break;
                }
            }

            if (match)
                return i;
        }

        return -1;
    }

    private int FindBytes(byte[] haystack, byte[] needle, int stride = 1)
    {
        int haystackLen = haystack.Length;
        int needleLen = needle.Length;

        if (needleLen > haystackLen)
            return -1;

        for (int i = 0; i <= haystackLen - needleLen; i += stride)
        {
            bool match = true;
            for (int j = 0; j < needleLen; j++)
            {
                if (haystack[i + j] != needle[j])
                {
                    match = false;
                    break;
                }
            }

            if (match)
                return i;
        }

        return -1;
    }

