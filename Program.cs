    private void AddFlashPattern(string flashType, byte[] identPattern, (byte[], byte[])[] patches)
    {
        var pattern = new FlashPatternInfo
        {
            FlashType = flashType,
            IdentPattern = new PatternInfo { Pattern = identPattern, PatternLength = identPattern.Length },
            Patches = new List<PatchInfo>(),
            PatchCount = patches.Length
        };

        foreach (var (marker, replace) in patches)
        {
            pattern.Patches.Add(new PatchInfo
            {
                Marker = new PatternInfo { Pattern = marker, PatternLength = marker.Length },
                Replace = new PatternInfo { Pattern = replace, PatternLength = replace.Length }
            });
        }

        flashPatterns.Add(pattern);
    }

