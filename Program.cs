    static void Main(string[] args)
    {
        if (args.Length == 0)
        {
            PrintUsage();
            return;
        }

        string romPath = args[0];

        if (!File.Exists(romPath))
        {
            Console.WriteLine($"Error: ROM file not found: {romPath}");
            return;
        }

        if (!romPath.ToLower().EndsWith(".gba"))
        {
            Console.WriteLine("Error: File must be a .gba file");
            return;
        }

        try
        {
            var patcher = new GBASaveFixPatcher();
            patcher.PatchROM(romPath, args);
        }
        catch (Exception ex)
        {
            Console.WriteLine($"Error: {ex.Message}");
        }
    }

    private void PatchROM(string romPath, string[] args)
    {
        // Parse arguments
        bool applySram = args.Contains("--sram");
        bool applyBattery = args.Contains("--battery");
        string batteryMode = args.Contains("--keypad") ? "keypad" : "auto";

        // If no specific options provided, apply both
        if (!applySram && !applyBattery)
        {
            applySram = true;
            applyBattery = true;
        }

        Console.WriteLine($"Loading ROM: {romPath}");
        byte[] romData = File.ReadAllBytes(romPath);
        Console.WriteLine($"ROM size: {romData.Length / (1024 * 1024.0):F2} MB");

        // Validate ROM size
        if (romData.Length > 0x02000000)
        {
            throw new Exception("ROM file too large (max 32MB)");
        }

        // Apply patches
        if (applySram)
        {
            Console.WriteLine("Applying SRAM patches...");
            romData = ApplySramPatches(romData);
            Console.WriteLine("SRAM patches applied successfully");
        }

        if (applyBattery)
        {
            Console.WriteLine($"Applying battery patches (mode: {batteryMode})...");
            romData = ApplyBatteryPatches(romData, batteryMode);
            Console.WriteLine("Battery patches applied successfully");
        }

        // Generate output filename
        string fileName = Path.GetFileNameWithoutExtension(romPath);
        string outputPath = GenerateOutputFilename(fileName, applySram, applyBattery, batteryMode);

        // Save patched ROM
        File.WriteAllBytes(outputPath, romData);
        Console.WriteLine($"Patched ROM saved: {outputPath}");
        Console.WriteLine("Done!");
    }

    private string GenerateOutputFilename(string baseName, bool sramEnabled, bool batteryEnabled, string batteryMode)
    {
        string suffix;

        if (sramEnabled && batteryEnabled)
        {
            suffix = batteryMode == "auto" ? "_ready_auto.gba" : "_ready_keypad.gba";
        }
        else if (sramEnabled)
        {
            suffix = "_sram_only.gba";
        }
        else if (batteryEnabled)
        {
            suffix = batteryMode == "auto" ? "_battery_auto.gba" : "_battery_keypad.gba";
        }
        else
        {
            suffix = "_no_patches.gba";
        }

        return $"{baseName}{suffix}";
    }

